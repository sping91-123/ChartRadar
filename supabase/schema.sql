as $$
declare
  v_id uuid;
begin
  perform pg_catalog.pg_advisory_xact_lock(pg_catalog.hashtextextended('news-impact-sync-v1', 0));
  if exists (
    select 1 from public.news_sync_runs run
    where run.lease_expires_at > pg_catalog.now()
