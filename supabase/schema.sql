create or replace function public.enforce_journal_monitor_owner()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.monitor_id is not null and not exists (
    select 1 from public.perpetual_scenario_monitors monitor
    where monitor.id = new.monitor_id and monitor.user_id = new.user_id
  ) then
    raise exception 'journal_monitor_owner_mismatch' using errcode = '23503';
  end if;
  return new;
end
$$;

