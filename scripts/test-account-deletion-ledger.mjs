import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import { join } from "node:path";
import { PGlite } from "@electric-sql/pglite";

const root = process.cwd();
const migration = readFileSync(
  join(root, "supabase/migrations/20260715164525_account_deletion_requests.sql"),
  "utf8"
) + "\n" + readFileSync(
  join(root, "supabase/migrations/20260717143000_harden_account_deletion_retry.sql"),
  "utf8"
);
const userId = "20000000-0000-4000-8000-000000000001";
const db = await PGlite.create();

async function serviceQuery(sql, params = []) {
  await db.exec("set role service_role");
  try {
    return await db.query(sql, params);
  } finally {
    await db.exec("reset role");
  }
}

try {
  await db.exec(`
    create schema auth;
    create role anon nologin;
    create role authenticated nologin;
    create role service_role nologin bypassrls;
    create or replace function auth.uid() returns uuid language sql stable as $$
      select nullif(current_setting('request.jwt.claim.sub', true), '')::uuid
    $$;
    create table auth.users (id uuid primary key, email text);
    create table public.profiles (id uuid primary key references auth.users(id) on delete cascade);
    create table public.journals (id uuid primary key default gen_random_uuid(), user_id uuid not null references auth.users(id));
    create table public.subscriptions (id uuid primary key default gen_random_uuid(), user_id uuid not null references auth.users(id));
    create table public.billing_entitlement_events (
      id bigint generated by default as identity primary key,
      user_id uuid references auth.users(id),
      provider text not null,
      event_id text not null
    );
    insert into auth.users (id,email) values ('${userId}','deletion@example.invalid');
    insert into public.profiles (id) values ('${userId}');
  `);
  await db.exec(migration);
  await db.exec(migration);

  const first = await serviceQuery("select * from public.request_account_deletion($1)", [userId]);
  assert.equal(first.rows[0].request_status, "pending");
  assert.ok(new Date(first.rows[0].process_after).getTime() - new Date(first.rows[0].requested_at).getTime() >= 6.9 * 86_400_000);

  const repeated = await serviceQuery("select * from public.request_account_deletion($1)", [userId]);
  assert.equal(repeated.rows[0].request_id, first.rows[0].request_id, "pending request must be idempotent");
  assert.equal(new Date(repeated.rows[0].requested_at).getTime(), new Date(first.rows[0].requested_at).getTime(), "a repeated request must preserve its original request time");
  assert.equal(new Date(repeated.rows[0].process_after).getTime(), new Date(first.rows[0].process_after).getTime(), "a repeated request must preserve its promised deadline");
  assert.equal((await db.query("select count(*)::int as count from public.account_deletion_requests")).rows[0].count, 1);

  const started = await serviceQuery("select * from public.start_account_deletion($1)", [first.rows[0].request_id]);
  assert.equal(started.rows[0].request_status, "processing");
  const leaseBlocked = await serviceQuery("select * from public.start_account_deletion($1)", [first.rows[0].request_id]);
  assert.equal(leaseBlocked.rows.length, 0, "an active processing lease must not be stolen");
  await db.query("update public.account_deletion_requests set started_at=now()-interval '16 minutes' where id=$1", [first.rows[0].request_id]);
  const leaseRecovered = await serviceQuery("select * from public.start_account_deletion($1)", [first.rows[0].request_id]);
  assert.equal(leaseRecovered.rows[0].request_status, "processing", "a stale processing lease must be recoverable");
  const whileProcessing = await serviceQuery("select * from public.request_account_deletion($1)", [userId]);
  assert.equal(whileProcessing.rows[0].request_id, first.rows[0].request_id, "processing request must be reused");
  assert.equal((await serviceQuery("select public.cancel_account_deletion($1) as canceled", [userId])).rows[0].canceled, false);

  await serviceQuery("select public.fail_account_deletion($1,'provider unavailable')", [first.rows[0].request_id]);
  const failed = await db.query("select status,next_attempt_at from public.account_deletion_requests where id=$1", [first.rows[0].request_id]);
  assert.equal(failed.rows[0].status, "failed");
  assert.ok(new Date(failed.rows[0].next_attempt_at).getTime() > Date.now(), "provider failures must receive retry backoff");
  const backoffBlocked = await serviceQuery("select * from public.start_account_deletion($1)", [first.rows[0].request_id]);
  assert.equal(backoffBlocked.rows.length, 0, "automatic retry must wait for the backoff deadline");
  const retried = await serviceQuery("select * from public.request_account_deletion($1)", [userId]);
  assert.equal(retried.rows[0].request_status, "pending");
  assert.equal(retried.rows[0].request_id, first.rows[0].request_id);
  assert.equal(new Date(retried.rows[0].requested_at).getTime(), new Date(first.rows[0].requested_at).getTime(), "manual retry must preserve the original request time");
  assert.equal(new Date(retried.rows[0].process_after).getTime(), new Date(first.rows[0].process_after).getTime(), "manual retry must preserve the promised deadline");
  assert.equal((await serviceQuery("select public.cancel_account_deletion($1) as canceled", [userId])).rows[0].canceled, true);

  const afterCancel = await serviceQuery("select * from public.request_account_deletion($1)", [userId]);
  assert.notEqual(afterCancel.rows[0].request_id, first.rows[0].request_id, "canceled request starts a new lifecycle");

  await db.query("insert into public.journals (user_id) values ($1)", [userId]);
  await db.query("insert into public.subscriptions (user_id) values ($1)", [userId]);
  await db.query("insert into public.billing_entitlement_events (user_id,provider,event_id) values ($1,'manual','evt-delete')", [userId]);
  await serviceQuery("select public.purge_account_application_data($1)", [userId]);
  assert.equal((await db.query("select count(*)::int as count from public.journals where user_id=$1", [userId])).rows[0].count, 0);
  assert.equal((await db.query("select count(*)::int as count from public.subscriptions where user_id=$1", [userId])).rows[0].count, 0);
  assert.equal((await db.query("select count(*)::int as count from public.profiles where id=$1", [userId])).rows[0].count, 0);
  const anonymizedEvent = await db.query("select user_id,event_id from public.billing_entitlement_events limit 1");
  assert.equal(anonymizedEvent.rows[0].user_id, null);
  assert.match(anonymizedEvent.rows[0].event_id, /^anonymized:/, "audit event identifiers must not retain the user UUID");

  console.log("Account deletion request, retry, cancellation, and purge matrix passed.");
} finally {
  await db.close();
}
